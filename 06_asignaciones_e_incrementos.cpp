/* Asignaciones e incrementos */
#include <iostream>

int main(){
	int x, y;
	int z, a, b, c;
	x = 4;
	y = 7;
	
	std::cout << x << ' ' << y << std::endl;
	x = y + 3;
	std::cout << x << ' ' << y << std::endl;
	std::cout << (z - 2) * 10 << std::endl;
	std::cout << z << std::endl;
	a = b = c = 6; 
	std::cout << a << std::endl;
	
	return 0;
}
